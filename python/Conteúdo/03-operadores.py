#operadores de comparação

a = 10
b = 5

print('Operadores de Comparação')

print(a > b) # maior que 
print(a < b) # menor que
print(a == b) # igual
print(a != b) # diferente
print(a >= b) # maior ou igual
print(a <= b) # menor ou igual

# Operadores logicos 

numero = 10

#No AND o resultado é True se os dois lados forem True 

print('Operadores Lógicos')

print(numero > 5 and numero < 15)

# No OR o resultado é True somente se um dos lados forem True 

print(numero < 5 or numero == 10)

# NOT pega o bool original e nega ele 

print(not (numero > 5))

